#coisar o coiso do coisa lá e falar todas as características que tu quiser dele.
negocinho = str (input('Digite um valor: '))
print('Qual a classe do valor? ', type(negocinho))
print('Ele é número e/ou letra? ', negocinho.isalnum())
print('Ele está apenas com letras minúsculas? ', negocinho.islower())
print('Ele é um número decimal? ', negocinho.isdecimal())